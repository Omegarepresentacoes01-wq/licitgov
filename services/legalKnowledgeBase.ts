/**
 * Retorna o contexto legal relevante para o tipo de documento solicitado.
 * Esse contexto é injetado no prompt para evitar alucinações da IA.
 */
export function getLegalContextForDocType(docType: DocumentType): string {
  const base = `
=== BASE DE CONHECIMENTO LEGAL VERIFICADA ===
FONTE: Lei nº 14.133/2021 (Diário Oficial da União, 01/04/2021)
FONTE: Portal gov.br/compras — Modelos Oficiais AGU/SEGES
FONTE: Jurisprudência TCU consolidada

ATENÇÃO: Use APENAS os artigos, incisos e acórdãos listados abaixo como referência.
NÃO invente números de artigos, acórdãos ou instruções normativas que não estejam aqui.
Se o artigo exato não estiver na base, cite apenas o tema geral da lei, sem inventar número.

${LEI_14133_PRINCIPIOS}
${INSTRUCOES_NORMATIVAS}
${JURISPRUDENCIA_TCU}
`;

  switch (docType) {
    case DocumentType.DFD:
      return base + LEI_14133_DFD + LEI_14133_PLANEJAMENTO;

    case DocumentType.ETP:
      return base + LEI_14133_ETP + LEI_14133_PLANEJAMENTO + LEI_14133_HABILITACAO;

    case DocumentType.MAPA_RISCO:
      return base + LEI_14133_MAPA_RISCO + LEI_14133_CONTRATOS + LEI_14133_FISCALIZACAO;

    case DocumentType.TR:
      return base + LEI_14133_TR + LEI_14133_HABILITACAO + LEI_14133_MODALIDADES + LEI_14133_CONTRATOS;

    case DocumentType.VIABILIDADE:
      return base + LEI_14133_ETP + LEI_14133_PLANEJAMENTO + LEI_14133_MAPA_RISCO;

    default:
      return base + LEI_14133_PLANEJAMENTO + LEI_14133_CONTRATOS;
  }
}

